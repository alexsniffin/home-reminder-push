func (c *Client) GetForecast() (*models.Forcast, error) {
	url := fmt.Sprintf("https://dark-sky.p.rapidapi.com/%s?units=auto&lang=en", longLat)

	req, _ := http.NewRequest(http.MethodGet, url, nil)
	req.Header.Add("X-RapidAPI-Key", c.apiKey)
	req.Header.Add("X-RapidAPI-Host", "dark-sky.p.rapidapi.com")
	res, err := http.DefaultClient.Do(req)
	if err != nil {
		return nil, fmt.Errorf("error making request: %w", err)
	}

	defer res.Body.Close()
	body, err := ioutil.ReadAll(res.Body)
	if err != nil {
		return nil, fmt.Errorf("error reading response: %w", err)
	}

	var forcast models.Forcast
	err = json.Unmarshal(body, &forcast)
	if err != nil {
		return nil, fmt.Errorf("error unmarshalling response: %w", err)
	}
	return &forcast, nil
}
